var v1 = 10, v2=3, res=0;

//Operador suma
res = v1+v2;
document.write("La suma de "+ v1 +" más "+ v2 +" es "+ res +" <br/>");

//Operador resta
res = v1-v2;
document.write("La resta de "+ v1 +" menos "+ v2 +" es "+ res +" <br/>");

//Operador multiplicación
res = v1*v2;
document.write("La multiplicación de "+ v1 +" por "+ v2 +" es "+ res +" <br/>");

//Operador división
res = v1/v2;
document.write("La división de "+ v1 +" entre "+ v2 +" es "+ res +" <br/>");

//Operador módulo
res = v1%v2;
document.write("El módulo de "+ v1 +" entre "+ v2 +" es "+ res +" <br/>");
